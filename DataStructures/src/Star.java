
public class Star {
	private double x, y, magnitude;
	
	public Star(double x, double y, double magnitude) {
		this.x = x;
		this.y = y;
		this.magnitude = magnitude;
	}
	
	public double getMagnitude() {
		return magnitude;
	}
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
}
